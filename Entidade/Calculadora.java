package util;

public class Calculadora {
	public int numero1;
	public int numero2;
	public int numero3;
	

    public int adicao1() {
    	return numero1 + numero2;
    	
    }public int adicao2() {
    	return numero1 + numero2 + numero3;
    	
    }public int subtracao1() {
    	return numero1 - numero2;
    	
    }public int subtracao2() {
    	return numero1 - numero2 - numero3;
    	
    }public int multiplicacao1() {
    	return numero1 * numero2;
    	
    }public int multiplicacao2() {
    	return numero1 * numero2 * numero3;
    	
    }public int divisao1(){
    	return numero1 / numero2;
    	
    }public int divisao2() {
    	return numero1 / numero2 / numero3;
    }

}
